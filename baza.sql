    JOIN rodzajeSiedziba r ON k.rodzaj_id = r.rodzaj_id
    UNION ALL
    SELECT kf.kurs_id, rf.cena 
    FROM kursyFilia kf
    JOIN rodzajeFilia rf ON kf.rodzaj_id = rf.rodzaj_id
) r_all ON u.kurs_id = r_all.kurs_id;

