    FROM kursyFilia kf
    JOIN rodzajeFilia rf ON kf.rodzaj_id = rf.rodzaj_id
    JOIN wykladowcyFilia wf ON kf.wykladowca_id = wf.wykladowca_id
) koszty_v;


